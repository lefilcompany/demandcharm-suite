#!/usr/bin/env node
// Generates public/firebase-config.generated.js from VITE_FIREBASE_* env vars.
// Never include VAPID key, service account, or any other secret here — this
// file is fetched by the browser and by the FCM service worker.
import { writeFileSync, mkdirSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { loadEnv } from "vite";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");
const OUT_PATH = resolve(ROOT, "public", "firebase-config.generated.js");

const MODE = process.argv[2] || process.env.NODE_ENV || "development";

// Merge order: process env wins over .env files loaded by Vite.
const fileEnv = loadEnv(MODE, ROOT, "VITE_");
const env = { ...fileEnv, ...process.env };

const REQUIRED = [
  "VITE_FIREBASE_API_KEY",
  "VITE_FIREBASE_AUTH_DOMAIN",
  "VITE_FIREBASE_PROJECT_ID",
  "VITE_FIREBASE_STORAGE_BUCKET",
  "VITE_FIREBASE_MESSAGING_SENDER_ID",
  "VITE_FIREBASE_APP_ID",
];

const header =
  "// AUTO-GENERATED by scripts/generate-firebase-config.mjs. Do not edit.\n" +
  "// Public Firebase config only. Never place secrets or VAPID key here.\n";

const missing = REQUIRED.filter((k) => !env[k] || String(env[k]).trim() === "");


mkdirSync(dirname(OUT_PATH), { recursive: true });

if (missing.length > 0) {
  writeFileSync(
    OUT_PATH,
    header + "self.__FIREBASE_CONFIG__ = null;\n",
    "utf8"
  );
  console.warn(
    `[firebase-config] Missing env vars: ${missing.join(", ")}. ` +
      "Wrote null config; push notifications will be disabled at runtime."
  );
  process.exit(0);
}

const config = {
  apiKey: env.VITE_FIREBASE_API_KEY,
  authDomain: env.VITE_FIREBASE_AUTH_DOMAIN,
  projectId: env.VITE_FIREBASE_PROJECT_ID,
  storageBucket: env.VITE_FIREBASE_STORAGE_BUCKET,
  messagingSenderId: env.VITE_FIREBASE_MESSAGING_SENDER_ID,
  appId: env.VITE_FIREBASE_APP_ID,
};

writeFileSync(
  OUT_PATH,
  header + "self.__FIREBASE_CONFIG__ = " + JSON.stringify(config, null, 2) + ";\n",
  "utf8"
);
console.log(`[firebase-config] Wrote ${OUT_PATH} (mode=${MODE})`);

