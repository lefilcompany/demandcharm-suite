#!/usr/bin/env node
// Generates public/firebase-config.generated.js from Firebase public env vars.
// The browser bundle prefers VITE_FIREBASE_* while Lovable Cloud exposes the
// same values to scripts as FIREBASE_*. This generator supports both shapes.
// Never include service account JSON or any private credential here — this file
// is fetched by the browser and by the FCM service worker. The VAPID key is a
// Firebase Web Push public key and is safe to expose to the browser.
import { writeFileSync, mkdirSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { loadEnv } from "vite";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");
const OUT_PATH = resolve(ROOT, "public", "firebase-config.generated.js");

const MODE = process.argv[2] || process.env.NODE_ENV || "development";

// Merge order: process env wins over .env files loaded by Vite.
const fileEnv = {
  ...loadEnv(MODE, ROOT, "VITE_"),
  ...loadEnv(MODE, ROOT, "FIREBASE_"),
};
const env = { ...fileEnv, ...process.env };

const PUBLIC_KEYS = [
  "FIREBASE_API_KEY",
  "FIREBASE_AUTH_DOMAIN",
  "FIREBASE_PROJECT_ID",
  "FIREBASE_STORAGE_BUCKET",
  "FIREBASE_MESSAGING_SENDER_ID",
  "FIREBASE_APP_ID",
  "FIREBASE_VAPID_KEY",
];

function readFirebasePublicValue(key) {
  const viteKey = `VITE_${key}`;
  const value = env[viteKey] || env[key];
  return typeof value === "string" && value.trim() !== "" ? value.trim() : "";
}

const header =
  "// AUTO-GENERATED by scripts/generate-firebase-config.mjs. Do not edit.\n" +
  "// Public Firebase config only. Never place secrets or VAPID key here.\n";

const missing = PUBLIC_KEYS.filter((k) => !readFirebasePublicValue(k));


mkdirSync(dirname(OUT_PATH), { recursive: true });

if (missing.length > 0) {
  writeFileSync(
    OUT_PATH,
    header + "self.__FIREBASE_CONFIG__ = null;\n",
    "utf8"
  );
  console.warn(
    `[firebase-config] Missing env vars: ${missing.join(", ")}. ` +
      "Wrote null config; push notifications will be disabled at runtime."
  );
  process.exit(0);
}

const config = {
  apiKey: readFirebasePublicValue("FIREBASE_API_KEY"),
  authDomain: readFirebasePublicValue("FIREBASE_AUTH_DOMAIN"),
  projectId: readFirebasePublicValue("FIREBASE_PROJECT_ID"),
  storageBucket: readFirebasePublicValue("FIREBASE_STORAGE_BUCKET"),
  messagingSenderId: readFirebasePublicValue("FIREBASE_MESSAGING_SENDER_ID"),
  appId: readFirebasePublicValue("FIREBASE_APP_ID"),
  vapidKey: readFirebasePublicValue("FIREBASE_VAPID_KEY"),
};

writeFileSync(
  OUT_PATH,
  header + "self.__FIREBASE_CONFIG__ = " + JSON.stringify(config, null, 2) + ";\n",
  "utf8"
);
console.log(`[firebase-config] Wrote ${OUT_PATH} (mode=${MODE})`);

